/**
 * The class name contract for the desktop component
 */
export interface DesktopClassNameContract {
    /**
     * The root of the component
     */
    desktop?: string;

    /**
     * The window
     */
    window?: string;

    /**
     * The window control item
     */
    window_control_item?: string;

    /**
     * The window control
     */
    window_control?: string;

    /**
     * The window title
     */
    window_title?: string;

    /**
     * The tab
     */
    window_tab?: string;

    /**
     * The navigation
     */
    navigation?: string;

    /**
     * The navigation control
     */
    navigation_control?: string;

    /**
     * The navigation address
     */
    navigation_address?: string;
}
